package id.web.bitocode.eu4provincewiki.model;

public class StateModel
{
  private String Name;
  private Long Total_Tax;
  private Long Total_Production;
  private Long Total_Manpower;
  private Long Total_Territory;
  
  public String getName()
  {
    return Name;
  }
  
  public void setName(String name)
  {
    Name = name;
  }
  
  public Long getTotal_Tax()
  {
    return Total_Tax;
  }
  
  public void setTotal_Tax(Long total_Tax)
  {
    Total_Tax = total_Tax;
  }
  
  public Long getTotal_Production()
  {
    return Total_Production;
  }
  
  public void setTotal_Production(Long total_Production)
  {
    Total_Production = total_Production;
  }
  
  public Long getTotal_Manpower()
  {
    return Total_Manpower;
  }
  
  public void setTotal_Manpower(Long total_Manpower)
  {
    Total_Manpower = total_Manpower;
  }
  
  public Long getTotal_Territory()
  {
    return Total_Territory;
  }
  
  public void setTotal_Territory(Long total_Territory)
  {
    Total_Territory = total_Territory;
  }
}
